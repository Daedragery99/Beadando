def kodol(msg):
    msg=msg.upper()
    c=""
    for i in msg:
        if i != " ":
            c+=szotar[i]+" "
        else:
            c+="    "
    return c

def dekodol(morse):
    szoveg = ""
    c = ""

    for index, i in enumerate(morse.strip() + " "):
        if i != " ":
            c += i
        elif c:
            szoveg += dekszotar[c]
            c = ""
            if index < len(morse) - 2:
                if morse[index: index + 4] == "    ":
                    szoveg += " "

    return szoveg

